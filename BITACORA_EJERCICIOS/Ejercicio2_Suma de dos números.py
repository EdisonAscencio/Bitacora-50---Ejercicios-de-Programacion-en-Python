'''
Ejercicio 2: Suma de dos números

Crea un programa que tome dos números como entrada del usuario
y muestre la suma de ambos.
'''

num1 = input('Digite el primer numero para sumar: ')
num2 = input('Digite el segundo numero para sumar: ')

resultado = int(num1)+int(num2)

print(f'El resultado de la suma del numero {num1} y {num2} es igual a {resultado}')